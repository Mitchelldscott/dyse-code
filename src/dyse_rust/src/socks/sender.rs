use dyse_rust::socks::sockapi::*;
use std::env;

fn main() {
    let mut args: Vec<String> = env::args().collect();
    args.remove(0);

    let name = args[0].clone();
    args.remove(0);

    let data = args.iter().map(|arg| arg.parse::<u8>().unwrap()).collect();

    let mut sock = SockApi::sender(&name);
    sock.tx_payload(data);
}
