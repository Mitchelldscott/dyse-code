use dyse_rust::comms::socks::*;
use pyo3::prelude::*;

#[pyfunction]
fn send(name: &str, data: Vec<f64>) -> PyResult<()> {
    let mut sock = Sockage::sender(&name);
    sock.send(data);
    Ok(())
}

#[pymodule]
fn socks(_py: Python, m: &PyModule) -> PyResult<()> {
    m.add_function(wrap_pyfunction!(send, m)?)?;
    Ok(())
}
